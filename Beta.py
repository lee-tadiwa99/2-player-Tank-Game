def move(Object,user_input):
    x = Object[1]
    y = Object[2]
    for i in range(0,4):
        if user_input == movements[i]:
            user_input = i
            continue
    if user_input == 0:
        Object[1] -= 1
    if user_input == 1:
        Object[2] -= 1
    if user_input == 2:
        Object[2] += 1
    if user_input == 3:
        Object[1] += 1
    if Map[Object[2]][Object[1]] == '|':
        Object[1] = x
    if Map[Object[2]][Object[1]] == '-':
        Object[2] = y
    if (Map[Object[2]][Object[1]] == '#') and (user_input == 0 or user_input == 3):
        Object[1] = x
    if (Map[Object[2]][Object[1]] == '#') and (user_input == 1 or user_input == 2):
        Object[2] = y
    Map[y][x] = ' '
    Map[Object[2]][Object[1]] = Object[0] 

